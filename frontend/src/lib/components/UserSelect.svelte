<script lang="ts" generics="T">
	import Check from "lucide-svelte/icons/check";
	import ChevronsUpDown from "lucide-svelte/icons/chevrons-up-down";
	import * as Command from "$lib/components/ui/command";
	import * as Popover from "$lib/components/ui/popover";
	import { Button } from "$lib/components/ui/button";
	import { cn } from "$lib/utils.js";
	import { tick } from "svelte";
    import Label from "./ui/label/label.svelte";
    import UserSearch from "./UserSearch.svelte";
   
	let { label, values, searchText = "Search", placeholder = "Select", value = $bindable(null) }: { label: string, values: { id: string, full_name: string }[], searchText: string, placeholder: string, value: { id: string, full_name: string } | null } = $props();
   
	let open = $state(false);
   
	const selectedValue = $derived(values.find((f) => f.id === value?.id)?.full_name);
   
	// We want to refocus the trigger button when the user selects
	// an item from the list so users can continue navigating the
	// rest of the form with the keyboard.
	function closeAndFocusTrigger(triggerId: string) {
		open = false;
		tick().then(() => {
			document.getElementById(triggerId)?.focus();
		});
	}
</script>

<div class="w-full">
	<Label>{label}</Label>
	<Popover.Root class="w-full" bind:open let:ids>
		<Popover.Trigger asChild let:builder>
		<Button
			builders={[builder]}
			variant="outline"
			role="combobox"
			aria-expanded={open}
			class="w-full font-normal justify-between border"
		>
			{#if selectedValue !== undefined}
				{selectedValue}
			{:else}
				<span class="text-muted-foreground font-normal">
					{placeholder}
				</span>
			{/if}
			<ChevronsUpDown class="ml-2 h-4 w-4 shrink-0 opacity-50" />
		</Button>
		</Popover.Trigger>
		<Popover.Content class="p-0">
			<UserSearch bind:value {values}/>
		</Popover.Content>
	</Popover.Root>
</div>